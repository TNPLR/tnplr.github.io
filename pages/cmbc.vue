                    <p>當首攻結束以後，如果下一次有機會出這個花色，且沒有要吃到該墩（例如沒有一定要出大牌時），且「出信號不會虧時」，可以信號同伴。在第一次出大牌的時候，第二次出原本的第四大牌（如果剩下兩張時出比較大的一張牌）。在第一次出大排第四張時，如果原本有第五張牌，出原本的第五大牌。如果原本是三張小牌的時候，出最大牌；四張以上小牌時，出原本的第四大牌。</p>
