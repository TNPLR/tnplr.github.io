            <ul>
                <li>非倒叫的二線新花 = 4張以上的牌組</li>
                <li>非倒叫的三線新花 = 5張以上的牌組，15點以上</li>
                <li>倒叫的二線新花 = 4張以上牌組，15點以上</li>
                <li>2NT = 12-14點或18-19點，未叫花色有擋張</li>
                <li>支持到3線 = 3張以上好支持</li>
                <li>跳叫新花 = Splinter，配合且該花色短門。</li>
